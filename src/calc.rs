// This is a simple calculator module

pub fn calc_sum( a: f64, b: f64 ) -> f64 {
	let sum = a + b;

	return sum;
}

pub fn calc_diff( a: f64, b: f64 ) -> f64 {
	let diff = a - b;

	return diff;
}

pub fn calc_prod( a: f64, b: f64 ) -> f64 {
	let prod = a * b;

	return prod;
}

pub fn calc_res( a: f64, b: f64 ) -> f64 {
	let res = a / b;

	return res;
}
